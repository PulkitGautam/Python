# Reversing a List
grade = ['Excellent', 'Good', 'Average', 'Fail']

grade.reverse()
print(grade)

# Finding Length of a List
num = len(grade)
print(num)

# Maximum, Minimum number in List
numbers = [24, 69, 12, 9, 15, 6]
print(min(numbers))
print(max(numbers))

# Sum of Numbers in a List
print(sum(numbers))