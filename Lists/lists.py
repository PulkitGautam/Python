# Lists or Arrays
cities = ['Delhi', 'Mumbai', 'Kolkata', 'Chennai']
print(cities)

# Acessing elements
print(cities[0])
print(cities[1])
print(cities[2])
print(cities[3])

# Using Methods
print(cities[2].upper())

# Accessing elements from last
print(cities[-1])                      #last element
print(cities[-2])                      #second last element